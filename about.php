2005-06: Private equity investment in DTDC by Reliance ADAG<br>

2007: Revenue crossed the 200-cr. mark<br>

2008:	Introduction of Premium Express Products (PEP) portfolio<br>

2009-10:	DTDC's Supply Chain Solutions division was launched with a special focus on providing Cargo Services via surface, air and rail modes.<br>

2010:	DTDC Retail Ltd. was launched in 2010. Under its aegis, DTDC Retail Ltd. set up DTDC New World that operates a chain of convenience retail stores that endeavors to create a world of convenience by providing a bouquet of services to make customer’s lives easy.<br>

2012:	DTDC acquires a majority stake in Eurostar Express of UAE - first international acquisition by an Indian express company<br>

2013: DTDC enters into strategic partnership with Europe’s leading logistics player ‘Geopost’, the Express arm of La Poste Group, for extensive penetration into key markets across the world.<br>
DTDC launched its specialist logistics company for e-commerce - DotZot - the 1st pan India e-commerce delivery network.<br>
DTDC acquired the Bangalore-based freight forwarding company Nikkos Logistics Pvt Ltd to enhance its freight presence globally<br>

2014:	DTDC undertakes an organization-wide Business Transformation Program, with its Engagement Partner IBM on its 25th year<br>

2015:	DTDC launched the new brand identity through a strategic partner – DPD group. The brand name is re-christened to DTDC Express Limited (formerly known as DTDC Courier & Cargo Ltd.) along with a new Cobranded identity DTDC sets up its first state-of-the-art automated hub at Hyderabad, followed by Delhi.</p>
